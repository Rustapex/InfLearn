package loop.ex;

public class whileEx2 {
    public static void main(String[] args) {
        int numW =2;

        for(int num =2; num <= 20; num+=2){
            System.out.println(num);
        }
        System.out.println("\n");

        while(numW <= 20){
            System.out.println(numW);
            numW +=2;
        }

    }
}
